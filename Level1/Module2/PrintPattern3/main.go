package main

import (
	"fmt"
)

/*
	*
*		*
*		*
	*

*/

func main() {

	var n int
	fmt.Scan(&n)

	for i := 1; i <= n; i++ {
		for j := i; j < n; j++ {
			fmt.Print(" ")
		}
		for k := 1; k <= i; k++ {
			fmt.Print(" * ")
		}
		fmt.Println()
	}

	for i := n; i >= 1; i-- {
		for j := i; j < n; j++ {
			fmt.Print(" ")
		}
		for k := 1; k <= i; k++ {
			fmt.Print(" * ")
		}
		fmt.Println()
	}
}
